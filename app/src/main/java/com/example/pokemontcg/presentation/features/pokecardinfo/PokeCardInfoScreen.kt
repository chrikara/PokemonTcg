@Composable
fun PokeCardInfoScreen(
    pokeId : String,
    navController: NavController,
    viewModel : PokeCardInfoViewModel = hiltViewModel()
) {




    if(viewModel.state.pokeInfoCard!=null)
        PokeCardInfo(pokeInfoCard = viewModel.state.pokeInfoCard!!, navController = navController)


    LaunchedEffect(key1 = listOf(
        viewModel.state.pokeInfoCard,
        viewModel.state.energyInfoCard,
        viewModel.state.trainerInfoCard,
        viewModel.state.error,
    )) {
        viewModel.getPokeCardInfoByPokemonIdFromAPI(pokeId)
    }



    println(viewModel.state)
}